class Arithmetic:

    
    def add(self, x, y):
        """ Addizione """
        return x + y

    
    def division(self, x, y):
        """ Divisione """
        return x / y

    
    def multiply(self, x, y):
        """ Moltiplicazione """
        return x * y

    
    def subtract(self, x, y):
        """ Sottrazione """
        return x - y

    
    def module(self, x, y):
        """ Modulo """
        return x % y

    
    def resto(self, x, y):
        """ Resto """
        return x ** y

    
    def intero(self, x, y):
        """ intero """
        return x // y
